package Ejercicio5;

public class Persona {
	
	protected String nombre;
	protected int edad;
	protected String sexo;
	protected int calificacion;
	
	public Persona(String nombre, int edad, String sexo, int calificacion) {

		this.nombre = nombre;
		this.edad = edad;
		this.sexo = sexo;
		this.calificacion = calificacion;
	}
	
}
